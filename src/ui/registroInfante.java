    private void resto() {
        String[] fecha = new String[3];
        String[] fechaIngreso = new String[3];
        nombreInfante.setToolTipText("nombre(s) y apellido(s)");
        numeroDeMatricula.setToolTipText("Numero de matricula");
        alergiaInfante.setToolTipText("Alergias que posee el infante");
        if (queTipo == 1) {
            btnAnadir.setVisible(false);
            btnCancelar.setVisible(false);
            /**
             *llenar los arrays
             * obtener los datos
             * eliminar los datos en la posicion indicada
             * lleno un txt
             * 
             */
            
            Matricula matricula = base.getInfante(numMatricula);
            Acudiente acudiente = base.getAcudiente(numMatricula);
            PersonaCosteaMensualidad perCostea = base.getperCostea(numMatricula);
            
            String auxi = matricula.fechaNacimiento;
            fecha = auxi.split("/");
            
            auxi = matricula.fechaIngresoGuarderia;
            fechaIngreso = auxi.split("/");
            numeroDeMatricula.setText(matricula.numeroMatricula);
            numeroDeMatricula.setEditable(false);
            numeroDeMatricula.setToolTipText("Numero de matricula no editable");
            alergiaInfante.setText(matricula.alergiaDelInfante);
            nombreInfante.setText(matricula.nombreDeInfante);
            siSedioDeBaja.setText(matricula.siSeDioBajaAlNinoFecha);
            
            nombreAcudiente.setText(acudiente.Nombre);
            direccionAcudiente.setText(acudiente.direccion);
            telefonAcudiente.setText(acudiente.numeroTelefono);
            DNIAcudiente.setText(acudiente.DNI);
            relacionConElInfante.setText(acudiente.relacionConInfante);
            
            direccionCostea.setText(perCostea.acudiente.direccion);
            nombreCostea.setText(perCostea.acudiente.Nombre);
            DNICostea.setText(perCostea.acudiente.DNI);
            numeroDeCuenta.setText(perCostea.numeroDeCuenta);
            telefonCostea.setText(perCostea.acudiente.numeroTelefono);

           
            
            
      
        } else {
            siSedioDeBaja.setEditable(false);
            siSedioDeBaja.setText("Inexistente");
            btnAceptar.setVisible(false);

            Date daano = new Date();
            SimpleDateFormat fe = new SimpleDateFormat("dd-MM-YYYY");
            String auxi = String.valueOf(fe.format(daano));
            fecha = auxi.split("-");

        }

        ///   Modelo dia JSpinner
        modeloDia = new SpinnerNumberModel();
        modeloDia.setMaximum(31);
        modeloDia.setMinimum(1);
        modeloDia.setValue(Integer.parseInt(fecha[0]));
        spidia.setModel(modeloDia);
        spidia.setEditor(new JSpinner.DefaultEditor(spidia));

        SpinnerNumberModel modeloMes = new SpinnerNumberModel();
        modeloMes.setMaximum(12);
        modeloMes.setMinimum(1);
        modeloMes.setValue(Integer.parseInt(fecha[1]));
        spiMes.setModel(modeloMes);
        spiMes.setEditor(new JSpinner.DefaultEditor(spiMes));

        SpinnerNumberModel modeloAnio = new SpinnerNumberModel();
        modeloAnio.setMaximum(3000);
        modeloAnio.setMinimum(1910);
        modeloAnio.setValue(Integer.parseInt(fecha[2]));
        spiAnio.setModel(modeloAnio);
        spiAnio.setEditor(new JSpinner.DefaultEditor(spiAnio));

        //*********************************************************************
        //*********************************************************************
        //                   Fecha de ingreso
        SpinnerNumberModel modeloDiaIngreso = new SpinnerNumberModel();
        modeloDiaIngreso.setMaximum(31);
        modeloDiaIngreso.setMinimum(1);
        if (queTipo == 1) {
            modeloDiaIngreso.setValue(Integer.parseInt(fechaIngreso[0]));
        } else {
            modeloDiaIngreso.setValue(Integer.parseInt(fecha[0]));
        }
        spidiaIngreso.setModel(modeloDiaIngreso);
        spidiaIngreso.setEditor(new JSpinner.DefaultEditor(spidiaIngreso));

        SpinnerNumberModel modeloMesIngreso = new SpinnerNumberModel();
        modeloMesIngreso.setMaximum(31);
        modeloMesIngreso.setMinimum(1);
        if (queTipo == 1) {
            modeloMesIngreso.setValue(Integer.parseInt(fechaIngreso[1]));
        } else {
            modeloMesIngreso.setValue(Integer.parseInt(fecha[1]));
        }
        spiMesIngreso.setModel(modeloMesIngreso);
        spiMesIngreso.setEditor(new JSpinner.DefaultEditor(spiMesIngreso));

        SpinnerNumberModel modeloAnioIngreso = new SpinnerNumberModel();
        modeloAnioIngreso.setMaximum(3000);
        modeloAnioIngreso.setMinimum(1910);
        if (queTipo == 1) {
            modeloAnioIngreso.setValue(Integer.parseInt(fechaIngreso[2]));
        } else {
            modeloAnioIngreso.setValue(Integer.parseInt(fecha[2]));
        }
        spiAnioIngrso.setModel(modeloAnioIngreso);
        spiAnioIngrso.setEditor(new JSpinner.DefaultEditor(spiAnioIngrso));
    }
