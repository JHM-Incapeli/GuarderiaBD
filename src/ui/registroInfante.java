    private boolean btnAceptarYAnadir() {
        boolean confi = true;
        if (nombreInfante.getText().equals("") || alergiaInfante.getText().equals("")
                || numeroDeMatricula.getText().equals("") ||/*Acudiente*/ nombreAcudiente.getText().equals("")
                || direccionAcudiente.getText().equals("") || telefonAcudiente.getText().equals("")
                || DNIAcudiente.getText().equals("") || relacionConElInfante.getText().equals("")
                ||/*Acudiente*/ /*Persona costea*/ direccionCostea.getText().equals("") || nombreCostea.getText().equals("")
                || DNICostea.getText().equals("") || numeroDeCuenta.getText().equals("")
                || telefonCostea.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "No deje campo sin rellenar \n"
                    + "si el dato no existe digite Inexistente ", "Aviso", JOptionPane.WARNING_MESSAGE);
           confi=false;
        } 
        Icon icono = new ImageIcon(getClass().getResource("/imgjpanel" + File.separator + "escoger.png"));
        int confir = JOptionPane.showConfirmDialog(null, "Confirme",
                "Confirme", JOptionPane.YES_NO_OPTION, JOptionPane.DEFAULT_OPTION, icono);
        if (confir == JOptionPane.YES_OPTION) {
            confi = true;
        }
        return confi;
    }

