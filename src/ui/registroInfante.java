    private void incluirdatos() {
        String fechaNAcimeiento = String.valueOf(spidia.getValue()) + "/" + String.valueOf(spiMes.getValue())
                + "/" + String.valueOf(spiAnio.getValue());
        String fechaIngreso = String.valueOf(spidiaIngreso.getValue()) + "/"
                + String.valueOf(spiMesIngreso.getValue()) + "/" + String.valueOf(spiAnioIngrso.getValue());

        Acudiente acudiente = new Acudiente(DNIAcudiente.getText(), nombreAcudiente.getText(),
                direccionAcudiente.getText(), telefonAcudiente.getText(),
                relacionConElInfante.getText());

        PersonaCosteaMensualidad per = new PersonaCosteaMensualidad(numeroDeCuenta.getText());
        per.acudiente = new Acudiente(DNICostea.getText(), nombreCostea.getText(), direccionCostea.getText(),
                telefonCostea.getText(), "");

        Matricula infate = new Matricula(numeroDeMatricula.getText(), nombreInfante.getText(), fechaNAcimeiento, fechaIngreso,
                siSedioDeBaja.getText(), alergiaInfante.getText());
        if(base.addInfate(infate, acudiente, per,queTipo)== true){
            numeroDeMatricula.setText("");nombreInfante.setText("");alergiaInfante.setText("");
            nombreAcudiente.setText("");direccionAcudiente.setText("");telefonAcudiente.setText("");
            DNIAcudiente.setText("");relacionConElInfante.setText("");direccionCostea.setText("");
            nombreCostea.setText("");DNICostea.setText("");numeroDeCuenta.setText("");telefonCostea.setText("");
        }
        
        
    }
