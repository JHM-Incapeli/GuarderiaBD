    private void nombreCosteaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nombreCosteaKeyTyped
        char c = evt.getKeyChar();
        if (Character.isDigit(c) || c == '!') {
            evt.consume();
        }
        if (nombreCostea.getText().length() == 0 && c == ' ') {
            evt.consume();
        }
        if (nombreCostea.getText().endsWith(" ")) {// el ultimo caracter es un espacio
            if (c == ' ') {
                evt.consume();
            }
        }
        if(nombreCostea.getText().length()==200){
        evt.consume();
        JOptionPane.showMessageDialog(null,"Maximo de caracteres 200","Aviso",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_nombreCosteaKeyTyped

